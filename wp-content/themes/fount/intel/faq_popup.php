    <div class="faq-expert-panel">
        <h2>FAQ</h2>
        <div class="faq-container">
            <h3>How do I find all reports on Gold exploration during the last month?</h3>
            <ul>
                <li>Open the ‘Date range’ filter and either select ‘30’ or enter the specific dates in the ‘From’ / ‘To’ boxes</li>
                <li>Open the ‘Project Status’ filter and select ‘Exploration Stage’</li>
                <li>Open the ‘Commodity’ filter and select ‘Gold’</li>
                <li>The map is now displaying all gold exploration reports released in the last month</li>
                <li>Click on any pin on the map to view the summary of that particular report</li>
                <li>Click on the ‘Download – View this Report’ button to open the full report</li>
                <ul>
        </div>
        <div class="faq-container">
            <h3>How do I find a project or report from a particular company?</h3>
            <ul>
                <li>Click in the Search Box</li>
                <li>Select ‘Company’ from the drop-down menu</li>
                <li>Enter the company name</li>
                <li>Select the project(s) that you wish to view. You may select one, several or all projects from that company</li>
                <li>Click ‘Show on Map’</li>
                <li>Click on a pin on the map to view the summary of the report</li>
                <li>Click on the ‘Download – View this Report’ button to open the full report</li>
                <ul>
        </div>
        <div class="faq-container">
            <h3>How do I find reports on transactions for Lithium projects worth more than $1m?</h3>
            <ul>
                <li>Click on the ‘Transaction Reports’ tab at the top of the side bar. Note that you need to be a registered user to open the Transaction Reports tab</li>
                <li>Open the ‘Commodity’ filter and select ‘Lithium’</li>
                <li>Open the ‘Value Range’ filter and select ‘over $1,000,000’</li>
                <li>Click on any pin on the map to view the summary of that particular report</li>
                <li>Click on the ‘Download – View this Report’ button to open the full report. Note that downloading a transaction report uses part of your downloading credit. Different subscription plans have different downloading limits</li>
                <ul>
        </div>
        <div class="faq-container">
            <h3>How do I set my filter preferences?</h3>
            <ul>
                <li>Open the filters that you wish to select as your default search criteria</li>
                <li>Click on ‘Save current filters’</li>
                <li>Click on ‘OK’ in the pop-up window</li>
                <li>Your chosen filter set will now be saved and may be viewed or deleted in your profile under ‘Saved Preferences’</li>
                <ul>
        </div>
        <div class="faq-container">
            <h3>How do I change my subscription?</h3>
            <ul>
                <li>Click on the Avatar in the top right-hand side of the screen</li>
                <li>Click on ‘Upgrade’ in the membership details</li>
                <li>The subscription plans will be displayed</li>
                <li>Select the required plan</li>
                <ul>
        </div>
        <div class="faq-container">
            <h3>How do I add or change my password?</h3>
            <ul>
                <li>Click on the Avatar in the top right-hand side of the screen</li>
                <li>Click on ‘Manage Profile’</li>
                <li>Click on ‘Reset password’ under the Personal Information heading</li>
                <li>Enter your new password in the change password screen</li>
                <li>Confirm your new password</li>
                <li>Click on ‘Change password’</li>
                <li>Your password has now been changed</li>
                <li>To return to the map click on ‘Back to opaxe’</li>
                <ul>
        </div>
        <div class="faq-container">
            <h3>How do I add or change my profile photo?</h3>
            <ul>
                <li>Click on the Avatar in the top right-hand side of the screen to open your profile</li>
                <li>Click on ‘Manage Profile’</li>
                <li>Click on ‘Upload your photo’ under the Profile Photo text</li>
                <li>Select your photo. Note the size requirements shown on the profile page</li>
                <li>Click ‘Save Changes’</li>
                <li>Your profile photo is now active</li>
                <li>To change your photo, go to your profile page</li>
                <li>Click ‘Remove’ next to the profile photo and follow the steps above</li>
                <ul>
        </div>
    </div>
</div>