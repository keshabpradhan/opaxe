                <h3>Opaxe concept</h3>
                <p>
                    The Opaxe map and database was originally designed and developed by RSC as a free intelligence tool for the mineral industry.
                </p>
                <p>
                    By monitoring public reports and displaying these spatially, it can be easily visualised, for example, which commodities are being reported on most, which geographic locations are most active, how many new resources are reported, and which reporting codes are used. It then becomes easy to generate trend plots, for instance, showing trends in reporting activity. As a source of information, the map and databases are a valuable resource for activities such as target acquisition, market research, resource comparison, and for investors, regulators and codes (AusIMM, AIG, ASX, JORC, NI43-101, etc).
                </p>
                <p>
                    Opaxe acquired the RSC Mineral Intelligence service in 2019. We are continually adding reports to the databases in all categories, increasing the value of our service to our users.
                </p>

                <div id="reports-database" class="fixed-headers"></div>
                <h3>Types of reports included in the database</h3>
                <p>
                    Two categories of reports are included; Technical Reports concerning mineral exploration and mining projects; and Transaction Reports concerning reported transactions of mineral projects at any stage of development.
                    Technical reports mostly includes reports that are positioned on the central part of the mining value chain (Figure 1). Projects where substantial exploration work is being completed are of main interest and therefore public reports on the results of early exploration activities such as mapping or soil sampling programmes are not included. Similarly, results for established mines (such as reserve updates based on different cut-off grades) are not included as they have little bearing on the “activity monitor”.
                </p>
                <p>
                    Transaction reports covers transactions of any size/value of mineral projects at all stages of development from exploration through to developed mines on care and maintenance.
                </p>
                <p>
                    We also monitor Environmental Impact Assessment reports (EIAs, Code -unrelated) and reports supporting acquisitions (mostly NI 43-101).
                </p>
