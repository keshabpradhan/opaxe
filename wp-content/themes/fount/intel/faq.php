                <div id="frequency-updates" class="fixed-headers"></div>
                <h3>Update frequency</h3>
                <p>
                    We update our databases on a daily basis. We do not provide a ‘real-time’ service, we aim to make most new reports available within 72 hours of their publication.
                </p>


                <div id="download-links" class="fixed-headers"></div>
                <h3>Links to public reports</h3>
                <p>
                    Each project pin on the map includes the download link to the public report. Where possible, we try to include the technical Documentation that goes with the Public Report. For NI43-101 reports, this will be added when it is made available through SEDAR up to 40 days after the Public Report. In the case of JORC, this documentation may not always be available for download.
                </p>

                <div id="codes" class="fixed-headers"></div>
                <h3>Codes</h3>
                <p>
                    Most of the public reporting is based on the NI 43-101 and JORC Codes. These are displayed on the map using either an “N” or “J” within the pin. Other Codes are displayed using an “O”. There may be reports where the Code affiliation may not be provided. This may be a breach of listing rules or otherwise may simply be because the reporting company is private. We treat “public” announcements by private companies as Public Reports.
                </p>

