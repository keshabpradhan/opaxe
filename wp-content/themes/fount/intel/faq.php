                <h2>What is Opaxe?</h2>

                <p>
                    Opaxe is an online interactive map and database repository that displays and stores public reports for mineral exploration and mining projects globally. The majority of the projects included are undertaken by companies listed on the major global stock exchanges.
                </p>

                <p>
                    Mineral exploration projects are represented by pins on the map. When any pin is clicked a window shows all public reports (Announcements and Technical Reports) available for that project. Along with a download link to each report, the window shows summary information such as the resource statement, best drill intersections, name of the QP/CP, etc. All data displayed on the Opaxe interactive map is available in the public domain.
                    A database of Transaction Reports is also provided which displays and stores global reports published since April 2017 relating to transactions of mineral exploration, resource development, and mining properties, including property acquisitions, company acquisitions and takeovers. Reported transactions are represented by pins on the map. A summary of any transaction report may be viewed by clicking on a pin, and the full report may be downloaded from the summary. Viewing and downloading transaction reports is subject to membership of a relevant Opaxe subscription plan.
                </p>

                <p>
                    Filters are available for both the Technical Reports and the Transaction Reports to display user-defined subsets of the overall data. Projects can be filtered by various criteria including commodity type, date, project status, stock exchange the company is listed on, reporting code, report type, and transaction size and type. Users are able to set and save their preferred filters allowing immediate access to reports based on these preferences. A search tool is available allowing users to search for specific projects, code, competent persons and other relevant fields. Usage of pre-set and saved filters, together with the ability to view reports based on selections of commodities, project status, report type, stock exchange listing etc is subject to user registration to an Opaxe subscription plan.
                </p>

